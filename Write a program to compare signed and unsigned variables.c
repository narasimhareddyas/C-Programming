//Write a program to compare signed and unsigned variables.

#include <stdio.h>

int main()
{
    // Signed integer
    int a = -36;

    // Unsigned integer
    unsigned int b = 36;

    // Printing signed value
    printf("Signed a = %d\n", a);

    // Printing unsigned value
    printf("Unsigned b = %u\n", b);

    return 0;
}
